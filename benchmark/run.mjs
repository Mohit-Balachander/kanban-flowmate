import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const autocannon = require("autocannon");

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

function pct(part, total) {
  if (!total) return 0;
  return (part / total) * 100;
}

function ms(n) {
  // autocannon latency fields are in ms
  return typeof n === "number" ? n : null;
}

function estimateP95(latency) {
  if (!latency || typeof latency !== "object") return null;
  if (typeof latency.p95 === "number") return latency.p95;

  const p90 = latency.p90;
  const p97_5 = latency.p97_5;
  if (typeof p90 !== "number" || typeof p97_5 !== "number") return null;

  // Estimate p95 using linear interpolation between p90 and p97.5.
  const t = (95 - 90) / (97.5 - 90);
  return p90 + t * (p97_5 - p90);
}

function toFixed(n, digits = 2) {
  if (typeof n !== "number" || Number.isNaN(n)) return null;
  return Number(n.toFixed(digits));
}

async function runOne({ title, url, method = "GET", headers, body, duration = 20, connections = 25, pipelining = 1 }) {
  const start = Date.now();
  const result = await autocannon({
    url,
    method,
    headers,
    body,
    duration,
    connections,
    pipelining,
    percentiles: [50, 95, 99],
  });
  const elapsedMs = Date.now() - start;

  const totalRequests =
    result?.requests?.total ??
    result?.requests?.sent ??
    null;

  const errorCount =
    (result?.errors ?? 0) +
    (result?.timeouts ?? 0) +
    (result?.non2xx ?? 0);

  return {
    title,
    url,
    method,
    params: { duration, connections, pipelining },
    elapsedMs,
    totals: {
      requests: totalRequests,
      errors: result?.errors ?? 0,
      timeouts: result?.timeouts ?? 0,
      non2xx: result?.non2xx ?? 0,
    },
    metrics: {
      throughput_rps: toFixed(result?.requests?.average, 2),
      latency_avg_ms: ms(result?.latency?.average),
      latency_p95_ms: toFixed(estimateP95(result?.latency), 2),
      latency_p99_ms: ms(result?.latency?.p99),
      error_rate_pct: toFixed(pct(errorCount, totalRequests), 4),
    },
    raw: {
      latency: result?.latency,
      requests: result?.requests,
      throughput: result?.throughput,
    },
  };
}

async function main() {
  const baseUrl = process.env.BASE_URL || "http://localhost:8081";
  const outJson = path.join(__dirname, "results.json");
  const outMd = path.join(__dirname, "report.md");

  const now = new Date().toISOString();

  const targets = [
    {
      title: "Board API (GET /api/board)",
      url: `${baseUrl}/api/board`,
      method: "GET",
    },
    {
      title: "Metrics (GET /metrics)",
      url: `${baseUrl}/metrics`,
      method: "GET",
    },
  ];

  const runs = [];
  for (const t of targets) {
    // Run each test once; keep it reproducible and fast.
    // If you want heavier tests, increase duration/connections via env vars.
    const duration = Number(process.env.DURATION ?? 20);
    const connections = Number(process.env.CONNECTIONS ?? 25);
    const pipelining = Number(process.env.PIPELINING ?? 1);

    runs.push(
      await runOne({
        ...t,
        duration,
        connections,
        pipelining,
        headers: { Accept: "application/json" },
      }),
    );
  }

  const summary = {
    timestamp: now,
    baseUrl,
    params: {
      duration: Number(process.env.DURATION ?? 20),
      connections: Number(process.env.CONNECTIONS ?? 25),
      pipelining: Number(process.env.PIPELINING ?? 1),
    },
    results: runs,
  };

  await fs.mkdir(__dirname, { recursive: true });
  await fs.writeFile(outJson, JSON.stringify(summary, null, 2), "utf8");

  const lines = [];
  lines.push("## Benchmark Report");
  lines.push("");
  lines.push(`- **Timestamp**: \`${now}\``);
  lines.push(`- **Base URL**: \`${baseUrl}\``);
  lines.push(
    `- **Parameters**: duration=${summary.params.duration}s, connections=${summary.params.connections}, pipelining=${summary.params.pipelining}`,
  );
  lines.push("");
  lines.push("### Results");
  lines.push("");
  lines.push("| Endpoint | Avg latency (ms) | P95 (ms) | P99 (ms) | Throughput (req/s) | Error rate (%) |");
  lines.push("|---|---:|---:|---:|---:|---:|");

  for (const r of runs) {
    lines.push(
      `| ${r.title} | ${r.metrics.latency_avg_ms ?? ""} | ${r.metrics.latency_p95_ms ?? ""} | ${r.metrics.latency_p99_ms ?? ""} | ${r.metrics.throughput_rps ?? ""} | ${r.metrics.error_rate_pct ?? ""} |`,
    );
  }

  lines.push("");
  lines.push("### Notes");
  lines.push("");
  lines.push("- This report is generated by `benchmark/run.mjs` using `autocannon`.");
  lines.push("- If `p95` is not directly emitted by the load test tool, it is estimated from p90 and p97.5 and recorded as `latency_p95_ms`.");
  lines.push("- `error_rate_pct` counts timeouts, network errors, and non-2xx responses.");
  lines.push("");

  await fs.writeFile(outMd, lines.join("\n"), "utf8");

  process.stdout.write(`Wrote ${outJson}\nWrote ${outMd}\n`);
}

await main();

